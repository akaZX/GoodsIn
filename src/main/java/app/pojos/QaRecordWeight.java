package app.pojos;


public class QaRecordWeight {

  private int rowid;
  private double weight;
  private int boxes;


  public int getRowid() {
    return rowid;
  }

  public void setRowid(int rowid) {
    this.rowid = rowid;
  }


  public double getWeight() {
    return weight;
  }

  public void setWeight(double weight) {
    this.weight = weight;
  }


  public int getBoxes() {
    return boxes;
  }

  public void setBoxes(int boxes) {
    this.boxes = boxes;
  }

}
